      <p>Cilji:</p>
      <ul>
        <li>
          okrepitev watchdog vloge zagovorniških NVO, splošne javnosti in
          novinarjev pri spremljanju realizacije obljub izvoljenih
          predstavnikov,
        </li>
        <li>
          povečanje politične odgovornosti funkcionarjev in dvig politične
          kulture,
        </li>
        <li>povečanje politične ozaveščenosti državljank in državljanov.</li>
      </ul>
      <p>
        V Sloveniji že tradicionalno vlada veliko nezaupanje v delo politikov,
        kar je pomemben del krize demokracije. Nezaupanje poganja razočaranje
        nad politiko, kar v začaranem krogu ohranja šibko politično
        participacijo državljanov in nizko legitimnost odločevalcev ter vzdržuje
        nezadovoljstvo.
      </p>
      <p>
        Tudi volilna udeležba zadnjih 15 let upada. Parlamentarnih volitev se
        udeležuje približno polovica volivcev, predsedniških in lokalnih manj
        kot polovica, evropskih pa celo manj kot tretjina. Vse to omogoča
        pomanjkanje politične odgovornosti na vseh političnih ravneh, tako med
        političnimi strankami in njihovimi predstavniki kot tudi med zaposlenimi
        na ministrstvih. Da bi naslovili krizo politične odgovornosti, smo
        razvili spletno platformo Obljuba dela dolg, svojevrstno oglasno desko
        političnih obljub in nenehen opomin politikom, da za svoje besede in
        dejanja odgovarjajo javnosti.
      </p>
      <p>
        Obljuba dela dolg ni magična rešitev za probleme, ki se v slovenski
        politiki kopičijo že desetletja. Želimo pa si, da bi platforma volivcem
        olajšala spremljanje dela izvoljenih predstavnikov, spodbudila politično
        in volilno angažiranost javnosti, političnim funkcionarjem pa nastavila
        ogledalo, v katerem se bo zrcalil njihov trud za uresničevanje ciljev
        oziroma njegovo pomanjkanje. Obljuba dela dolg naj deluje tudi kot
        opomin, da javnost podrobno spremlja delo politikov, kar naj jih zaveže
        k večji politični odgovornosti.
      </p>
